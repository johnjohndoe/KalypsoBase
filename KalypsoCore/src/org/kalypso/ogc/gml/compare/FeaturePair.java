package org.kalypso.ogc.gml.compare;

import org.kalypsodeegree.model.feature.Feature;

/**
 * @author Holger Albert
 */
public class FeaturePair
{
  private Feature m_one;

  private Feature m_two;

  public FeaturePair( final Feature one, final Feature two )
  {
    m_one = one;
    m_two = two;
  }

  public Feature getOne( )
  {
    return m_one;
  }

  public Feature getTwo( )
  {
    return m_two;
  }

  public void setOne( final Feature one )
  {
    m_one = one;
  }

  public void setTwo( final Feature two )
  {
    m_two = two;
  }
}